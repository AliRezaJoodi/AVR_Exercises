float NTC100K_ConvertOhmToTemp(float ohm){
    unsigned int index=0;
    float temp=-1;
    char error=0;
    
    while(Table_NTC_100K[index] > ohm){
        ++index;
        if(index>300){error=1; break;};
    } 
    if(error==0){temp=index;} else{temp=-1;}; 
    
    return temp;
}

